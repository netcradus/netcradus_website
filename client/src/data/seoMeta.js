const SEO_META_INDEX = Object.values(SEO_META).filter((entry) => entry.path);

export function getSeoMetaByPath(pathname) {
  const safePath = pathname && pathname !== "" ? pathname : "/";
  const normalized = safePath === "/" ? "/" : safePath.replace(/\/+$/, "");

  if (normalized.startsWith("/blog/")) {
    return SEO_META.blogFallback;
  }

  return SEO_META_INDEX.find((entry) => entry.path === normalized) || null;
}

export function getBlogPostMeta(blog) {
  if (!blog) {
    return SEO_META.blogFallback;
  }

  return {
    title: `${blog.title} | Netcradus`,
    description: blog.seoDescription || SEO_META.blogFallback.description,
    keywords: blog.seoKeywords || SEO_META.blogFallback.keywords,
    type: "article",
  };
}
